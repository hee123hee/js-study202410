let x = 10;
console.log('초기 x:', x);  // 출력: 초기 x: 10

x += 5;  // x = x + 5
console.log('x += 5:', x);  // 출력: x += 5: 15

x -= 3;  // x = x - 3
console.log('x -= 3:', x);  // 출력: x -= 3: 12

x *= 2;  // x = x * 2
console.log('x *= 2:', x);  // 출력: x *= 2: 24

x /= 4;  // x = x / 4
console.log('x /= 4:', x);  // 출력: x /= 4: 6

x %= 5;  // x = x % 5
console.log('x %= 5:', x);  // 출력: x %= 5: 1